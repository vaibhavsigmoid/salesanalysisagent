CREATE TABLE raw_pos (
    sales_id INT,
    product_id INT,
    store_id INT,
    product_name VARCHAR(100),
    brand VARCHAR(50),
    category VARCHAR(50),
    volume_ml INT,
    alcohol_percentage DECIMAL(5, 2),
    store_name VARCHAR(100),
    city VARCHAR(50),
    state VARCHAR(10),
    region VARCHAR(20),
    store_type VARCHAR(20),
    sales_date DATE,
    quantity_sold INT,
    unit_price DECIMAL(10,2),
    total_sales DECIMAL(12,2),
);
